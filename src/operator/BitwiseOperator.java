package operator;

public class BitwiseOperator {
public static void main(String[] args) {
	int a=12, b=15;
	System.out.println(a&b);
	System.out.println(a|b);
	System.out.println(a^b);
	
	a=5;
	b=5;
	System.out.println(a&b);
	System.out.println(a|b);
	System.out.println(a^b);

	a=20;
	b=25;
	System.out.println(a&b);
	System.out.println(a|b);
	System.out.println(a^b);
}
}
